    public function getRadioButtons(Request $request)
    {
        $programId = $request->input('program_id');
        $program = Program::find($programId);
        if (!$program) {
            return response()->json(['error' => 'Program not found'], 404);
        }

        $durationQty = $program->duration;
        return response()->json(['duration' => $durationQty]);
    }
